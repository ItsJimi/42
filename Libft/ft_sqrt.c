#include "libft.h"

int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	if (nb > 0 && nb < 2147483647)
	{
		while (i * i != nb)
		{
			i++;
			if (i * i > nb)
				return (0);
		}
		return (i);
	}
	else
		return (0);
}
