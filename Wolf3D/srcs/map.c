#include "wolf3d.h"

void	map(t_mlx *mlx)
{
	int		x;
	int		y;

	y = 0;
	while (y < mlx->h)
	{
		x = 0;
		while (x < mlx->w)
		{
			if (mlx->p[y][x]->z == 1)
			{
				draw_img_map(mlx, 0xffffff, x, y);
			}
			x++;
		}
		y++;
	}
}
