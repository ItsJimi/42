#include "libft.h"

void    ft_putlong(long n)
{
    if (n < 0)
    {
        ft_putchar('-');
        n = -n;
    }
    if (n == 0)
        ft_putchar('0');
    if (n / 10)
        ft_putlong(n / 10);
    ft_putchar(n % 10 + '0');
}