int	ft_sqrt(int nbr)
{
	int nb;
	int i;

	nb = 0;
	i = 0;
	if (nbr < 0)
		return (0);
	while (nb < nbr)
	{
		nb = i * i;
		i++;
	}
	if (i * i == nbr)
		return (i - 1);
	else
		return (0);
}
