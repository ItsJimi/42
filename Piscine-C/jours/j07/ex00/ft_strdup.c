#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int i;
	int *p;

	i = 0;
	p = (char *)(malloc(sizeof(*src)));
	while (src[i] != '\0')
	{
		p[i] = src[i];
		i++;
	}
	return (p);
}
